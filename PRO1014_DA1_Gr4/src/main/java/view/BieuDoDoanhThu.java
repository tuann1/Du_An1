    public BieuDoDoanhThu(DefaultTableModel tableShow) {
        initComponents();
        setLocationRelativeTo(null);
        Image icon = Toolkit.getDefaultToolkit().getImage("src/main/resources/icon/vodien.png");
        this.setIconImage(icon);
        chart.addLegend("Doanh thu", new Color(135, 189, 245));

        for (int j = 0; j < tableShow.getRowCount(); j++) {

            chart.addData(new ModelChart((int) tableShow.getValueAt(j, 0) + "",
                    new double[]{
                        BigDecimal.valueOf(Double.valueOf(fomartFloat(tableShow.getValueAt(j, 2).toString()))).doubleValue(),}));
        }
        chart.start();
    }
    
